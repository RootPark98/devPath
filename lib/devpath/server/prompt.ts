import type { RequestBody } from "./input";

/**
 * 프롬프트 생성 레이어
 * - route.ts에서 긴 프롬프트 문자열을 떼어내서 가독성을 높임
 * - 나중에 프롬프트 A/B 테스트나 버전 관리도 쉬워짐
 */

export function buildPrompt(body: RequestBody): string {
  const frameworksLine = body.frameworks.length > 0 ? body.frameworks.join(", ") : "선택 없음";

  return `
너는 개발자 취준생을 위한 전문 포트폴리오 프로젝트 설계자다.
단순한 토이 프로젝트가 아니라, 실제 실무에서 겪는 고민(데이터 흐름, 상태 관리, 예외 처리, 성능/확장성, 유지보수)을 드러낼 수 있는 프로젝트를 설계한다.

출력 규칙(매우 중요):
- 반드시 JSON 객체 1개만 출력하라.
- 마크다운 코드 펜스(\`\`\`json 등), 설명 문장, 추가 텍스트를 절대 금지한다. (JSON.parse가 즉시 가능해야 함)
- 배열로 감싸지 말 것.

출력 언어 규칙:
- 모든 문자열 값(projectTitle, oneLiner, mvpFeatures, buildSteps, readmeDraft, interviewPoints)은 반드시 한국어로 작성한다.
- 기술 용어(${body.language}, ${frameworksLine}, REST, API, DB 등)는 관례적으로 영어 표기를 유지해도 된다. 단, 문장은 한국어로 작성한다.
- README는 GitHub에서 그대로 렌더링 가능한 Markdown 형식으로 작성한다.

사용자 입력:
- 언어/스택: ${body.language}
- 난이도: ${body.level}
- 프레임워크/라이브러리(선택): ${frameworksLine}

아래 스키마에 맞는 결과를 생성하라:
{
  "projectTitle": "string",
  "oneLiner": "string",
  "mvpFeatures": ["string"],
  "buildSteps": ["string"],
  "readmeDraft": "string",
  "interviewPoints": ["string"]
}

프로젝트 생성 방식(중요):
- 먼저 "현실적인 문제 상황"을 하나 설정한다.
- 그 문제를 해결하기 위한 서비스 형태의 프로젝트를 설계한다.
- 단순 기능 구현이 아니라 **문제 해결 중심 서비스**여야 한다.

프로젝트 도메인 선택 규칙:
아래 서비스 도메인 중 하나를 선택하여 프로젝트를 설계한다.

가능한 도메인:
- 협업 도구
- 데이터 분석 플랫폼
- 커머스/결제
- AI 기반 서비스
- 교육 플랫폼
- 헬스케어
- 위치 기반 서비스
- 콘텐츠 플랫폼
- 커뮤니티/소셜 서비스
- 개발자 도구 (DevTool)
- 생산성 자동화 서비스
- 금융/가계부 서비스
- 로그 분석 / 모니터링 도구
- API 관리 플랫폼

금지 규칙:
- Todo 리스트
- 단순 게시판
- 메모 앱
- 기본 CRUD 예제
- 블로그 클론

위와 같은 단순 프로젝트는 생성하지 않는다.

작성 규칙:
- 전체 결과는 위 스키마 구조에 맞춰 작성한다.

projectTitle 규칙:
- 서비스처럼 보이는 이름을 만든다.
- 예: "DevInsight", "TeamFlow", "LogScope" 같은 형태
- 단순 기능 이름(예: 게시판, Todo 관리)은 사용하지 않는다.

난이도 반영 상세:
- 초급:
  - 핵심 스택의 기본 동작 원리
  - 클린 코드
  - 기본적인 에러 핸들링
  - 간단한 상태 관리

- 중급:
  아래 중 최소 1~2개 반드시 포함
  - 상태 관리 라이브러리
  - API 모킹
  - Unit Test
  - 데이터 캐싱
  - 데이터 페이징
  - 성능 최적화

- 고급:
  아래 중 최소 2개 이상 반드시 포함
  - CI/CD 파이프라인
  - 대용량 트래픽 처리 전략
  - 보안(OAuth / JWT)
  - 성능 모니터링
  - 로그 수집 및 분석
  - 마이크로서비스 고려

mvpFeatures 작성 규칙:
- 5~7개 작성
- 단순 기능이 아니라
  "어떤 기술로 어떤 문제를 해결하는지"가 드러나게 작성

buildSteps 작성 규칙:
- 6~10단계 작성
- 다음 흐름을 따르도록 한다

1. 개발 환경 및 프로젝트 구조 설정
2. 핵심 도메인 모델 설계
3. 주요 비즈니스 로직 구현
4. API 설계 및 데이터 흐름 구현
5. 예외 처리 및 데이터 검증
6. 테스트 전략 적용
7. 성능 또는 구조 개선
8. 배포 및 운영 고려

interviewPoints 작성 규칙:
- 최소 5개 작성
- 기술 면접관 관점에서 질문 생성

예시 질문 유형:
- 왜 이 기술을 선택했는가?
- 다른 기술과의 Trade-off는 무엇인가?
- 프로젝트 확장 시 어떤 구조로 리팩토링할 것인가?
- 성능 병목은 어디에서 발생할 수 있는가?
- 장애 상황을 어떻게 대응할 것인가?

또한 아래 내용을 최소 1개 이상 반드시 포함한다:
- 실무 환경을 가정한 기술적 제약 사항
- 실제 운영 중 발생할 수 있는 트러블슈팅 사례

readmeDraft 작성 규칙(매우 중요):

다음 항목을 이 순서대로 반드시 포함한다:

# 개요
## 주요 기능
## 기술 스택
## 실행 방법
## 폴더 구조
## 개선 아이디어

## 폴더 구조 규칙
- 반드시 트리 구조 코드블록을 사용한다.
- 코드블록 언어는 bash 또는 text를 사용한다.
- 최소 8줄 이상의 트리 구조를 작성한다.
- ├── └── │ 문자를 사용한다.
- 각 주요 폴더 옆에 역할 설명 주석을 추가한다.

예:
services/ # API 호출 로직 관리

폴더 구조는 반드시 코드블록 안에만 작성한다.
`.trim();
}

